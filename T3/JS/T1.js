function fillDate(){
    let new1 = new Date();
    let y = new1.getFullYear();
    let m = new1.getMonth() + 1;
    let d = new1.getDate();
    let h = new1.getHours();
    let mm = new1.getMinutes();

    function aux(num) {
        if (num < 10) {
            return "0" + num;
        }
        return num;
    }

    document.getElementById('dia-hora-inicio').value = y + '-' + aux(m) + '-' + aux(d) + ' ' + aux(h) + ':' + aux(mm);
    let fecha1 = document.getElementById("dia-hora-inicio").value;
    let container = document.getElementById("dia-hora-termino");
    let new_date = fecha1.split(' ');
    let new_hour = new_date[1].split(':');
    container.value = new_date[0] + " " + ((parseInt(new_hour[0]) + 3)%24).toString() + ":" + new_hour[1];
}

